export async function POST(request) {
  const { age, gender, budget, exclude } = await request.json();

  const budgetLabel = {
    "3000": "〜3,000円",
    "5000": "3,000〜5,000円",
    "10000": "5,000〜10,000円",
    "20000": "10,000〜20,000円",
    "30000": "20,000〜30,000円",
    "50000": "30,000円以上",
  }[budget];

  const genderLabel = {
    male: "男の子",
    female: "女の子",
    other: "性別問わず",
  }[gender];

  const ageLabel = {
    "0m1": "0〜1ヶ月（新生児期）の赤ちゃん",
    "2m6": "2〜6ヶ月（ねんね期）の赤ちゃん",
    "7m12": "7ヶ月〜1歳（おすわり〜たっち期）の赤ちゃん",
    "1to2": "1〜2歳（よちよち歩き）の子供",
    "3to5": "3〜5歳（幼稚園・保育園）の子供",
    "6to9": "6〜9歳（小学校低学年）の子供",
    "10to12": "10〜12歳（小学校高学年）の子供",
    "13to15": "13〜15歳（中学生）",
    "16to18": "16〜18歳（高校生）",
    "19to22": "19〜22歳（大学生）",
    "23to30": "23〜30歳",
  }[age];

  let userPrompt = `贈り先: ${ageLabel}の${genderLabel}\n予算: ${budgetLabel}\n\n上記の条件に合うプレゼントを3つ提案してください。`;

  if (exclude && exclude.length > 0) {
    userPrompt += `\n\n※以下の商品は既に提案済みなので除外してください：${exclude.join("、")}`;
  }

  const systemPrompt = `あなたはプレゼント選びの専門家です。贈り手は主におじいちゃん・おばあちゃん・おじさん・おばさんなど、トレンドに詳しくない世代です。
贈り先の年齢・性別・予算をもとに、「今の子供・若者に本当に喜ばれるもの」を3つ提案してください。

以下のJSON形式で回答してください。JSON以外は一切出力しないでください。
[
  {
    "name": "商品名（具体的なブランド名・商品名）",
    "reason": "なぜこれが喜ばれるか（40文字以内）",
    "price": "目安の価格帯（例: 3,000〜5,000円）",
    "searchQuery": "Amazon・楽天で検索するためのキーワード"
  }
]

基本ルール:
- 必ず今のトレンドを反映した具体的な商品を提案する
- 「図書カード」「現金」など無難すぎるものは避ける
- 年齢に合った提案をする（3歳と15歳では全く違う）
- 予算は絶対に守ること。指定された予算の範囲内に収まる商品のみ提案する。予算が「3,000〜5,000円」なら、5,000円を超える商品は提案しない。良い商品でも予算オーバーなら別の商品を選ぶこと
- 価格は正確に把握できないこともあるため、予算の上限ギリギリの商品は避け、確実に予算内に収まる商品を優先する。迷ったら安い方を提案すること
- ビジーカー、キックバイク、大型遊具など単価が高くなりがちな商品は、予算が10,000円以上の場合にのみ提案する
- 大事なのは高い商品を提案することではなく、予算内の商品を提案して実際に購入してもらうこと。予算オーバーの商品を見せても、購入にはつながらない
- searchQueryはAmazon/楽天で実際に検索して見つかる具体的なキーワードにする

安全基準ルール（最重要・必ず守ること）:
- 商品の一般的な対象年齢が贈り先の年齢より上の場合は、絶対に提案しない。例：ままごとセット（対象年齢3歳以上）を0〜2歳に提案するのは禁止
- 0〜2歳には、ままごとセット・ごっこ遊び・本・ブロック・工作キットなど対象年齢3歳以上の商品は提案しない
- 3歳未満には、トイレットペーパーの芯ほどのサイズの小さなパーツを含む商品は提案しない
- 0〜2歳向けには、舐めても安全な素材・STマーク付きの商品を優先する
- 0〜2歳向けには、ビーズやボタンで目を作ったぬいぐるみは提案しない。刺繍タイプを優先する
- 0〜6ヶ月向けには、肌に優しいタオル地のような柔らかい素材を優先する
- 口に入れたときに塗料が入りやすい商品は提案しない
- 3〜5歳向けには、色鉛筆やボールペンなど先端がとがった文房具は提案しない。クレヨンを優先する
- 提案する前に「この商品の対象年齢は贈り先の年齢に合っているか？」を必ず自己チェックすること

年齢別キャラクター選定ルール:
- 0〜2歳: 丸いフェイスラインと大きな目のキャラクターを優先する（アンパンマン、はらぺこあおむし、ミッフィーなど）。視覚が未発達なため、シンプルな形のキャラクターの方が認識しやすい
- 0〜2歳: キティちゃんなど複雑なデザインのキャラクターは提案しない。3歳以降に提案する
- 0〜2歳: パステルカラーのぬいぐるみより、赤・青・黄色などはっきりした鮮やかな色のぬいぐるみを優先する（視覚が弱く淡い色は認識しづらい）

キャラクター好みの年齢遷移（参考にして提案すること）:
- 女の子の傾向: アンパンマン（0〜2歳）→ ハローキティ・サンリオ・キティちゃんなど（3〜5歳）→ プリキュア・ディズニープリンセス（4〜6歳以降）
- 男の子の傾向: アンパンマン（0〜2歳）→ トーマス・ティラノサウルスなどの乗り物系（2〜4歳）→ 戦隊もの・特撮ヒーロー（4〜6歳以降）
- キャラクターものは現在放送中・展開中の作品を優先する

月齢・年齢別の提案ガイド（必ず従うこと）:
- 0〜1ヶ月（新生児期）: 赤ちゃん本人向けのおもちゃではなく、ママ・パパ向けの実用品を提案する。おむつケーキや産着、ガーゼのスタイ（よだれかけ）・セットなど。この時期の赤ちゃんはほぼ寝ているだけで意思表示ができないため、赤ちゃんの快適さと親の実用性を最優先する
- 2〜6ヶ月（ねんね期）: ぬいぐるみ・ガラガラ・ラトル（柔らかい素材）、タオル地のぬいぐるみなど赤ちゃんが目で追ったり手で掴むだけでもよいおもちゃを提案する。鮮やかな色を優先する
- 7ヶ月〜1歳（おすわり〜たっち期）: 手で掴んで振れるラトル、音の出る布絵本、お風呂用おもちゃなど。おすわりやつかまり立ちができるようになる時期なので、座った状態で遊べるものが適している
- 1〜2歳（よちよち歩き）: 手押し車、型はめパズル、積み木（角が丸いもの）、お風呂用おもちゃなど。自分で動きたい気持ちが芽生える時期、手先を使う遊びに夢中になる
- 3〜5歳（幼稚園・保育園）: ごっこ遊び・ねんど粘土・クレヨンなどの創作系、三輪車・乗り物系（三輪車・キックバイク）、キャラクターグッズ
- 6〜9歳: ブロック・工作キット・実験・本・ボードゲーム
- 10歳以上: 年齢に応じて、子供扱いされないものを選ぶ`;

  try {
    const res = await fetch("https://api.anthropic.com/v1/messages", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "x-api-key": process.env.ANTHROPIC_API_KEY,
        "anthropic-version": "2023-06-01",
      },
      body: JSON.stringify({
        model: "claude-sonnet-4-20250514",
        max_tokens: 1000,
        system: systemPrompt,
        messages: [{ role: "user", content: userPrompt }],
      }),
    });

    if (!res.ok) {
      return Response.json({ error: "API request failed" }, { status: 500 });
    }

    const data = await res.json();
    const raw = data.content.map((c) => c.text || "").join("");
    const parsed = JSON.parse(raw.replace(/```json|```/g, "").trim());

    return Response.json(parsed);
  } catch (e) {
    console.error(e);
    return Response.json({ error: "Internal server error" }, { status: 500 });
  }
}